package Maratona_Java;

public class Variaveis {
    public static void main(String[] args) {

        int idade = 20;
        double valorPassagem = 5.20;
        float valorTomate = 2.30f;
        String nome = "Andre";
        boolean verdadeiro = true;
        boolean falso = false;


        System.out.println("idade é: " + idade);
        System.out.println("Valor da passagem é: " + valorPassagem);
        System.out.println("Valor do Tomate é: " + valorTomate);
        System.out.println("Meu nome é: " + nome);
        System.out.println("O valor de verdadeiro é: " + verdadeiro);
        System.out.println("O valor de Falso é: " + falso);

    }
}
